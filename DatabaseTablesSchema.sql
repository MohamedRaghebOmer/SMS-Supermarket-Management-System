    BEGIN TRY
        DECLARE @CurrentQuantity DECIMAL(18, 3);

        SELECT @CurrentQuantity = [Quantity]
        FROM [dbo].[ReturnItems]
        WHERE [ReturnItemId] = @ReturnItemId;

        IF @CurrentQuantity IS NULL
        BEGIN
            SET @StatusCode = -2;
